// Find a maximum
function findMax(arr) {
    return Math.max(...arr);
}

// Example usage:
let numbers = [10, 20, 5, 30, 15];
let maxNumber = findMax(numbers);
console.log(maxNumber); // Output will be 30

// Find a minimum
function findMin(arr) {
    return Math.min(...arr);
}

// Example usage:
let minNumber = findMin(numbers);
console.log(minNumber); // Output will be 5
